#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { mkdtempSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import process from "node:process";

const repoRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const outputPath = path.join(repoRoot, "src", "NextFs", "NextFs.Font.Google.g.fs");
const nextVersion = process.argv[2] ?? "16.2.1";
const declPath = "package/dist/compiled/@next/font/dist/google/index.d.ts";

function sh(command, args, cwd) {
  return execFileSync(command, args, {
    cwd,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "inherit"],
  });
}

function escapeFsString(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

const workspace = mkdtempSync(path.join(tmpdir(), "nextfs-fonts-"));
const tarball = sh("npm", ["pack", `next@${nextVersion}`, "--silent"], workspace).trim();
const declarations = sh("tar", ["-xOf", tarball, declPath], workspace);
const fontNames = Array.from(declarations.matchAll(/export declare function ([A-Za-z0-9_]+)</g), (match) => match[1]);

if (fontNames.length === 0) {
  throw new Error(`No Google font exports found in ${declPath}`);
}

const lines = [];
lines.push("namespace NextFs");
lines.push("");
lines.push("open Fable.Core");
lines.push("");
lines.push("// Generated by tools/generate-google-font-bindings.mjs");
lines.push(`// Source: next@${nextVersion} ${declPath}`);
lines.push("module GoogleFont =");

for (const fontName of fontNames) {
  lines.push(`    [<Import("${escapeFsString(fontName)}", "next/font/google")>]`);
  lines.push(`    let private import_${fontName}: obj -> LoadedFont = jsNative`);
  lines.push("");
  lines.push(`    let ${fontName}(options: obj) : LoadedFont =`);
  lines.push(`        import_${fontName} options`);
  lines.push("");
}

writeFileSync(outputPath, `${lines.join("\n")}\n`, "utf8");
console.log(`Generated ${fontNames.length} Google font bindings at ${path.relative(repoRoot, outputPath)}`);
