#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { mkdtempSync, readdirSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import process from "node:process";

const repoRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const outputDir = path.join(repoRoot, "src", "NextFs");
const outputBaseName = "NextFs.Font.Google.g.fs";
const outputPath = path.join(outputDir, outputBaseName);
const outputChunkPrefix = "NextFs.Font.Google.Chunk";
const chunkSize = 48;
const nextVersion = process.argv[2] ?? "16.2.1";
const declPath = "package/dist/compiled/@next/font/dist/google/index.d.ts";

function sh(command, args, cwd) {
  return execFileSync(command, args, {
    cwd,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "inherit"],
  });
}

function escapeFsString(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

const workspace = mkdtempSync(path.join(tmpdir(), "nextfs-fonts-"));
const tarball = sh("npm", ["pack", `next@${nextVersion}`, "--silent"], workspace).trim();
const declarations = sh("tar", ["-xOf", tarball, declPath], workspace);
const fontNames = Array.from(declarations.matchAll(/export declare function ([A-Za-z0-9_]+)</g), (match) => match[1]);

if (fontNames.length === 0) {
  throw new Error(`No Google font exports found in ${declPath}`);
}

function writeChunkFile(fontChunk, chunkIndex) {
  const chunkName = `${outputChunkPrefix}${String(chunkIndex + 1).padStart(2, "0")}.g.fs`;
  const chunkPath = path.join(outputDir, chunkName);
  const lines = [];
  lines.push("namespace NextFs");
  lines.push("");
  lines.push("open Fable.Core");
  lines.push("");
  lines.push("// Generated by tools/generate-google-font-bindings.mjs");
  lines.push(`// Source: next@${nextVersion} ${declPath}`);
  lines.push(`[<AutoOpen>]`);
  lines.push(`module GoogleFontChunk${String(chunkIndex + 1).padStart(2, "0")} =`);
  lines.push("    type GoogleFont with");

  for (const fontName of fontChunk) {
    lines.push(`        [<Import("${escapeFsString(fontName)}", "next/font/google")>]`);
    lines.push(`        static member ${fontName}(options: obj) : LoadedFont = jsNative`);
    lines.push("");
  }

  writeFileSync(chunkPath, `${lines.join("\n")}\n`, "utf8");
  return chunkPath;
}

for (const existingFile of readdirSync(outputDir)) {
  if (existingFile.startsWith(outputChunkPrefix) && existingFile.endsWith(".g.fs")) {
    rmSync(path.join(outputDir, existingFile));
  }
}

const baseLines = [];
baseLines.push("namespace NextFs");
baseLines.push("");
baseLines.push("// Generated by tools/generate-google-font-bindings.mjs");
baseLines.push(`// Source: next@${nextVersion} ${declPath}`);
baseLines.push("type GoogleFont = GoogleFont");
writeFileSync(outputPath, `${baseLines.join("\n")}\n`, "utf8");

const chunkPaths = [];
for (let index = 0; index < fontNames.length; index += chunkSize) {
  chunkPaths.push(writeChunkFile(fontNames.slice(index, index + chunkSize), chunkPaths.length));
}

console.log(
  `Generated ${fontNames.length} Google font bindings across ${chunkPaths.length + 1} files at ${path.relative(repoRoot, outputDir)}`
);
